package edu.scripps.yates.shared.util;

import java.io.Serializable;

public class ProjectNamedQuery implements Serializable {
	/**
	 *
	 */
	private static final long serialVersionUID = 6041379146337091632L;
	private String name;
	private String query;
	private int index;

	public ProjectNamedQuery() {

	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the query
	 */
	public String getQuery() {
		return query;
	}

	/**
	 * @param query
	 *            the query to set
	 */
	public void setQuery(String query) {
		this.query = query;
	}

	/**
	 * @return the index
	 */
	public int getIndex() {
		return index;
	}

	/**
	 * @param index
	 *            the index to set
	 */
	public void setIndex(int index) {
		this.index = index;
	}
}
